						</li>
						<li>
							（2）拼配：按品质和等级要求进行分级拼配。
						</li>
						<li>
							（3）渥堆：根据茶叶等级和气候条件，进行渥堆发酵，适时翻堆散热，待叶色变褐，发出醇香即可。
						</li>
						<li>
							（4）汽蒸：渥堆适度茶叶经蒸汽蒸软，形成散茶。
