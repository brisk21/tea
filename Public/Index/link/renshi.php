					</p>
					<p>
						<label>1.初制加工工艺流程：</label><p>鲜叶→杀青→初揉→堆闷→复揉→干燥→毛茶</p>
					</p>
					<p>
						<label for="gongyi">2．初制加工技术要求：</label>					
						<li>
							（1）鲜叶：选用适制茶树品种芽叶为原料。
						</li>
						<li>
							（2）杀青：要均匀，杀青以叶质柔软，叶色转为暗绿色，青草气味基本消失为适度。
						</li>
						<li>
							（3）初揉：趁温揉捻至成条索。
						</li>
						<li>
							（4）堆闷：初揉结束后进行筑堆堆闷，当堆温达到55℃时，及时进行翻堆散热，当堆温降到30℃时再收拢筑堆，继续堆闷直到适度为止。
						</li>
						<li>
							（5）复揉：再次揉紧成条索。
						</li>
						<li>
							（6）干燥：干燥至茶叶含水分不超过15%，成为毛茶。
						</li>
					</p>
					
