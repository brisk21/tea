						<span>1、什么是六堡茶？</span><p>答：六堡茶属黑茶类，有一千五百多年历史，因原产和主产于广西梧州六堡镇而得名，六堡茶以“红、浓、陈、醇”而著称，有独特槟榔香气，越陈越佳。自2011年六堡茶获得国家地理标志产品专用标志后，规定了以广西壮族自治区梧州市现辖行政区域为产地范围，品种为苍梧县群体种、广西大叶种及其分离、选育出来的品种并按照六堡茶加工工艺（初制、精制）生产的才可以称为六堡茶。因此我定义的六堡茶是以苍梧县群体种的初制毛茶，经过后期的发酵和陈化形成的一种特殊的茶类。</p>
					</p>
					
					<p>
						<span>2、六堡茶有生茶和熟茶之分吗？</span> <p>答：从国家技术标准上来讲没有，但从六堡茶传统的加工工艺来区别还是有的，传统的六堡农家茶（毛茶）是未经人工渥堆发酵，自然陈化的，所以可以认定为生茶，而经过精制的六堡茶（厂茶）是经人工渥堆发酵加工而成的，可以认定为熟茶。这个问题我与过茶叶专家农博士及长期经营六堡茶的茶农、商家交流过，大家都认可这个定义是比较科学的。</p>
